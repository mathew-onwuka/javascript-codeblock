//DATATYPES

// PRIMITIVE DATATYPES
// STRING: A TEXT IN QUOTES
//  var, let and const used in declaring variables
let myName = "Mr Mathew Udo";
console.log(myName); //Output Mr Mathew Udo
var firstName = "Mathew";
console.log(firstName); //Output Mathew

// NUMBER : Numeric value
let age = 36;
console.log(age); //Output 36
const pi = 3.14;
console.log(pi); //Output 3.14

// BOOLEAN : TRUE OR FALSE
const isStudent = true;
console.log(isStudent); //Output true
